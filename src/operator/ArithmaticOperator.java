package operator;

public class ArithmaticOperator {

	public static void main(String[] args) {
		// ArithmaticOperators:
		// + Additive operator
		// - subtraction
		// * Multiplication
		// / Division
		// % Remainder
	
		int car1 =98;
		int car2=44;
		
		int subCar=car1+car2;
		int car3=car1-car2;
		int totalCar=car1*car2;
		int car4=car1/car2;
		int car5=car1%car2;
		
		System.out.println("Summation value is " + subCar);
		System.out.println("Subtract value is " + car3);
		System.out.println("Multiply value is " + totalCar);
		System.out.println("Dision value is " + car4);
		System.out.println("Remainder value is " + car5);

	}
		
	}


